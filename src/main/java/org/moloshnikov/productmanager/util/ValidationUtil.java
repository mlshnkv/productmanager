package org.moloshnikov.productmanager.util;

import org.moloshnikov.productmanager.model.AbstractBaseEntity;

import java.util.NoSuchElementException;

public class ValidationUtil {

    private ValidationUtil() {
    }

    public static void checkNotFoundWithId(boolean found, int id) {
        checkNotFound(found, "id=" + id);
    }


    public static void checkNotFound(boolean found, String arg) {
        if (!found) {
            throw new NoSuchElementException(arg);
        }
    }

    public static void checkNew(AbstractBaseEntity bean) {
        if (!bean.isNew()) {
            throw new IllegalArgumentException(bean + " must be new (id=null)");
        }
    }

    public static void assureIdConsistent(AbstractBaseEntity bean, int id) {
        if (bean.isNew()) {
            bean.setId(id);
        } else if (bean.getId() != id) {
            throw new IllegalArgumentException(bean + " must be with id=" + id);
        }
    }
}